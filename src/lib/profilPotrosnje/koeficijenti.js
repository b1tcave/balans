import { getLastSundayInMonth, getYear } from "../dateutils";
import { getPeriod } from "./period";

const koeficijentiZaDatum = date => {
    const marchDST = getLastSundayInMonth(3, getYear(date));
    if (date === marchDST) {
        return koeficijenti_dst_mart;
    }
    const octoberDST = getLastSundayInMonth(10, getYear(date));
    if (date === octoberDST) {
        return koeficijenti_dst_oktobar;
    }
    return koeficijenti;
};

export const getKoeficijenti = ({
    datum,
    tipMernogMesta,
    Kp: kategorijaPotrosnje
}) => {
    const lookup = koeficijentiZaDatum(datum);
    const period = getPeriod(datum);
    let koef;
    try {
        koef = lookup[kategorijaPotrosnje][tipMernogMesta][period];
    } catch (e) {
        console.error("Cannot get koef for ", {
            datum,
            period,
            tipMernogMesta,
            kategorijaPotrosnje
        });
    }
    return koef;
};

